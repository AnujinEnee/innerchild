// Analysis function (legacy - uses in-short for suppressed needs)
export function analyzeLuscherCombinations(
  firstSelection: number[],
  _secondSelection: number[]
): {
  desiredGoals: string[];
  currentSituation: string[];
  limitedCharacteristics: string[];
  suppressedNeeds: string[];
} {
  const results = {
    desiredGoals: [] as string[],
    currentSituation: [] as string[],
    limitedCharacteristics: [] as string[],
    suppressedNeeds: [] as string[],
  };

  if (!firstSelection || firstSelection.length < 8) {
    return results;
  }

  // Analyze first two positions (desired goals - +)
  const first = firstSelection[0];
  const second = firstSelection[1];

  // Check for two-color combination first
  const desiredCombo = `+${first}+${second}`;
  if (DESIRED_GOALS[desiredCombo]) {
    results.desiredGoals.push(DESIRED_GOALS[desiredCombo]);
  } else {
    // If no two-color combination, add individual interpretations
    if (DESIRED_GOALS[`+${first}`]) {
      results.desiredGoals.push(DESIRED_GOALS[`+${first}`]);
    }
    if (DESIRED_GOALS[`+${second}`]) {
      results.desiredGoals.push(DESIRED_GOALS[`+${second}`]);
    }
  }

  // Analyze middle positions (current situation - *)
  const third = firstSelection[2];
  const fourth = firstSelection[3];
  const fifth = firstSelection[4];
  const sixth = firstSelection[5];

  [third, fourth, fifth, sixth].forEach((color) => {
    if (CURRENT_SITUATION[`*${color}`]) {
      results.currentSituation.push(CURRENT_SITUATION[`*${color}`]);
    }
  });

  // Analyze positions 6-7 (limited characteristics - =)
  const seventh = firstSelection[6];
  const eighth = firstSelection[7];

  if (LIMITED_CHARACTERISTICS[`=${seventh}`]) {
    results.limitedCharacteristics.push(LIMITED_CHARACTERISTICS[`=${seventh}`]);
  }
  if (LIMITED_CHARACTERISTICS[`=${eighth}`]) {
    results.limitedCharacteristics.push(LIMITED_CHARACTERISTICS[`=${eighth}`]);
  }

  // Analyze last two positions (suppressed needs - -)
  // Extract in-short from the detailed object
  const suppressedSeventh = SUPPRESSED_NEEDS[`-${seventh}`];
  if (suppressedSeventh) {
    results.suppressedNeeds.push(suppressedSeventh["in-short"]);
  }
  const suppressedEighth = SUPPRESSED_NEEDS[`-${eighth}`];
  if (suppressedEighth) {
    results.suppressedNeeds.push(suppressedEighth["in-short"]);
  }

  return results;
}

// Main function to get comprehensive analysis
export function getComprehensiveLuscherAnalysis(
  firstSelection: number[],
  secondSelection: number[]
) {
  const analysis = analyzeLuscherCombinations(firstSelection, secondSelection);

  return {
    desiredGoals: analysis.desiredGoals.filter(Boolean),
    currentSituation: analysis.currentSituation.filter(Boolean),
    limitedCharacteristics: analysis.limitedCharacteristics.filter(Boolean),
    suppressedNeeds: analysis.suppressedNeeds.filter(Boolean),
  };
}

