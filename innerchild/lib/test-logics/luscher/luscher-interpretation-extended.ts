export interface LuscherFiveSectionResult {
  section1_desiredGoalsSingle: {
    title: string;
    key: string;
    interpretation: string;
  };
  section2_desiredGoalsCombo: {
    title: string;
    key: string;
    interpretation: string;
  };
  section3_currentSituation: {
    title: string;
    firstSelectionKey: string;
    firstSelectionInterpretation: string;
    secondSelectionKey: string;
    secondSelectionInterpretation: string;
  };
  section4_limitedCharacteristics: {
    title: string;
    firstSelectionKey: string;
    firstSelectionInterpretation: string;
    secondSelectionKey: string;
    secondSelectionInterpretation: string;
  };
  section5_suppressedNeeds: {
    title: string;
    key: string;
    physicalExplanation: string;
    psychologicalExplanation: string;
    inShort: string;
  };
}

/**
 * Get Luscher test result in 5 main sections
 *
 * Section 1: First color from first selection (+)
 * Section 2: First two colors from first selection (+X+Y)
 * Section 3: 3rd+4th from BOTH selections (2 results)
 * Section 4: 5th+6th from BOTH selections (2 results)
 * Section 5: If 8th colors match use single (-X), else use 7th+8th from second (-X-Y)
 */
export function getLuscherFiveSectionResult(
  firstSelection: number[],
  secondSelection: number[]
): LuscherFiveSectionResult {
  // Section 1: First color from first selection
  const section1Key = `+${firstSelection[0]}`;
  const section1Interpretation = DESIRED_GOALS[section1Key] || "";

  // Section 2: First two colors from first selection
  const section2Key = `+${firstSelection[0]}+${firstSelection[1]}`;
  const section2Interpretation = DESIRED_GOALS[section2Key] || "";

  // Section 3: Current Situation - 3rd+4th from BOTH selections
  // First selection: positions 2 and 3
  const section3FirstKey = `*${firstSelection[2]}*${firstSelection[3]}`;
  const section3FirstInterpretation = CURRENT_SITUATION[section3FirstKey] || "";
  // Second selection: positions 2 and 3
  const section3SecondKey = `*${secondSelection[2]}*${secondSelection[3]}`;
  const section3SecondInterpretation =
    CURRENT_SITUATION[section3SecondKey] || "";

  // Section 4: Limited Characteristics - 5th+6th from BOTH selections
  // First selection: positions 4 and 5
  const section4FirstKey = `=${firstSelection[4]}=${firstSelection[5]}`;
  const section4FirstInterpretation =
    LIMITED_CHARACTERISTICS[section4FirstKey] || "";
  // Second selection: positions 4 and 5
  const section4SecondKey = `=${secondSelection[4]}=${secondSelection[5]}`;
  const section4SecondInterpretation =
    LIMITED_CHARACTERISTICS[section4SecondKey] || "";

  // Section 5: Compare 8th colors; if same use single, else use 7th+8th from second
  const color8_first = firstSelection[7];
  const color8_second = secondSelection[7];
  const section5Key =
    color8_first === color8_second
      ? `-${color8_first}`
      : `-${secondSelection[6]}-${secondSelection[7]}`;
  const section5Data = SUPPRESSED_NEEDS[section5Key] || {
    "physical-explanation": "",
    "psychological-explanation": "",
    "in-short": "",
  };

  return {
    section1_desiredGoalsSingle: {
      title: "Хүсэж буй зорилго",
      key: section1Key,
      interpretation: section1Interpretation,
    },
    section2_desiredGoalsCombo: {
      title: "Хүсэж буй зорилго (хослол)",
      key: section2Key,
      interpretation: section2Interpretation,
    },
    section3_currentSituation: {
      title: "Одоогийн нөхцөл байдал",
      firstSelectionKey: section3FirstKey,
      firstSelectionInterpretation: section3FirstInterpretation,
      secondSelectionKey: section3SecondKey,
      secondSelectionInterpretation: section3SecondInterpretation,
    },
    section4_limitedCharacteristics: {
      title: "Хязгаарлагдсан шинж чанарууд",
      firstSelectionKey: section4FirstKey,
      firstSelectionInterpretation: section4FirstInterpretation,
      secondSelectionKey: section4SecondKey,
      secondSelectionInterpretation: section4SecondInterpretation,
    },
    section5_suppressedNeeds: {
      title: "Дарангуйлагдсан хэрэгцээ",
      key: section5Key,
      physicalExplanation: section5Data["physical-explanation"],
      psychologicalExplanation: section5Data["psychological-explanation"],
      inShort: section5Data["in-short"],
    },
  };
}
