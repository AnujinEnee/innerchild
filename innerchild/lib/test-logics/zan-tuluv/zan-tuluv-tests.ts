export function scoreZan(answers: {
  [questionId: number]: ZanAnswer;
}): ZanScores {
  const result: ZanScores = {
    hyperthymic: 0,
    dysthymic: 0,
    cyclothymic: 0,
    emotive: 0,
    demonstrative: 0,
    stuck: 0,
    pedantic: 0,
    anxious: 0,
    excitable: 0,
    irritable: 0,
    validity: 0,
  };
  const yesVal: ZanAnswer = "A";
  const noVal: ZanAnswer = "C";

  (Object.keys(ZanKeys) as ZanTypeKey[]).forEach((k) => {
    const { coef, yes, no } = ZanKeys[k];
    let x = 0;
    yes.forEach((qid) => {
      if (answers[qid] === yesVal) x += 1;
    });
    no.forEach((qid) => {
      if (answers[qid] === noVal) x += 1;
    });
    const val = coef * x;
    if (k === "validity") {
      result.validity = val;
    } else {
      (result as Record<string, unknown>)[k] = val;
    }
  });
  return result;
}

