// Zan Tölöviin test (Leonhard accentuations)
// All questions are Yes/No. Scoring uses keyed true/false indices with multipliers per type.

export type ZanAnswer = "A" | "C"; // A=Yes(Тийм), C=No(Үгүй) to align with other tests' patterns

export interface ZanQuestion {
  id: number;
  question: string;
}

export type ZanTypeKey =
  | "hyperthymic" // Эрч хүчтэй
  | "dysthymic" // Гутрамтгай
  | "cyclothymic" // Солигдомтгой
  | "emotive" // Мэдрэмтгий
  | "demonstrative" // Сурталчламтгай
  | "stuck" // Гацамтгай хөшүүн
  | "pedantic" // Нягтламтгай
  | "anxious" // Түгшимтгий
  | "excitable" // Хөөрөмтгий
  | "irritable" // Цочромтгой
  | "validity"; // Баталгаат чанар

export interface ZanScaleKey {
  coef: number; // multiplier
  yes: number[]; // questions that score when answered Yes
  no: number[]; // questions that score when answered No
}

export type ZanScores = Record<Exclude<ZanTypeKey, "validity">, number> & {
  validity: number;
};

