export interface GutralScore {
  totalScore: number;
}

export const calculateGutralScore = (answers: {
  [questionId: number]: string;
}): GutralScore => {
  let total = 0;
  Object.values(answers).forEach((v) => {
    const n = parseInt(v, 10);
    if (!isNaN(n)) total += n;
  });
  return { totalScore: total };
};
