export type BrainResultKey = "leftBrain" | "balanced" | "rightBrain";

export function calculateBrainResult(answers: { [questionId: number]: string }): BrainResultKey {
  const score: BrainScore = { A: 0, B: 0, C: 0 };
  Object.values(answers).forEach((v) => {
    if (v === "A" || v === "B" || v === "C") score[v]++;
  });
  const weighted = score.A * 1 + score.B * 2 + score.C * 3;
  if (weighted <= 45) return "leftBrain";
  if (weighted <= 60) return "balanced";
  return "rightBrain";
}
